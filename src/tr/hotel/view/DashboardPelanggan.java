    public DashboardPelanggan(String username) {
        initComponents();
        this.username = username; // Simpan username ke atribut
        System.out.println("Pengguna yang login: " + username); // Debugging

        // Tabel Kamar
        KamarController kc = new KamarController();
        DefaultTableModel dtm = kc.createTableKamar(); // Membuat model tabel kamar
        this.tblKamar.setModel(dtm); // Menetapkan model ke tabel Kamar
        kc.lihatKamar();  // Memuat data kamar dengan benar

        // Tabel Pesanan
        PelangganController pc = new PelangganController();
        DefaultTableModel dtm1 = pc.createTable(); // Membuat model tabel pesanan
        this.tblPesanan.setModel(dtm1); // Menetapkan model ke tabel Pesanan
        pc.lihatPesanan(username);  // Memuat data pesanan berdasarkan username
        
        DefaultTableModel dtm2 = pc.createTableRiwayat();
        this.tblRiwayat.setModel(dtm2);
        pc.lihatRiwayat(username);

        // Ambil data pelanggan dan isi form
        Pelanggan pl = pc.editAkun(username); // Mendapatkan data pelanggan
        if (pl != null) {
            // Debugging: Pastikan data pelanggan ditemukan
            System.out.println("Data pelanggan ditemukan: " + pl.getNama());

            // Menampilkan data pengguna pada form
            this.txtUsername.setText(pl.getUsername());
            this.txtPassword.setText(pl.getPassword());
            this.txtNama.setText(pl.getNama()); // Menggunakan nama lengkap
            this.txtNoHp.setText(pl.getNoHp()); // Menggunakan nomor HP
            this.txtEmail.setText(pl.getEmail());
        } else {
            System.out.println("Gagal memuat data pelanggan.");
        }
        
    }
    
