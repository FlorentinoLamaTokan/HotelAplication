    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        System.out.println("Tombol Cek Kamar ditekan...");
    
        String tipeKamar = cmbTipeKamar.getSelectedItem().toString();
        String checkIn = txtCheckIn.getText();
        String checkOut = txtCheckOut.getText();

        System.out.println("Tipe kamar: " + tipeKamar);
        System.out.println("Check-in: " + checkIn);
        System.out.println("Check-out: " + checkOut);

        // Validasi input
        if (tipeKamar.isEmpty() || checkIn.isEmpty() || checkOut.isEmpty()) {
            lblStatus.setText("Data belum lengkap!");
            lblHarga.setText(".....");
            return;
        }

        try {
            PelangganController pc = new PelangganController(/* Masukkan koneksi database Anda */);
            boolean cekKamar = pc.cekKamar(tipeKamar, checkIn, checkOut);
            System.out.println("Hasil cek kamar: " + cekKamar);

            if (cekKamar) {
                double totalHarga = pc.getTotalHarga();
                System.out.println("Total harga: " + totalHarga);
                String harga = String.format("Rp %,d", (long) totalHarga);
                lblStatus.setText("Tersedia");
                lblHarga.setText(harga);
            } else {
                lblStatus.setText("Tidak Tersedia");
                lblHarga.setText(".....");
            }
        } catch (Exception e) {
            e.printStackTrace();
            lblStatus.setText("Error");
            lblHarga.setText(".....");
        }
        
    


    }//GEN-LAST:event_jButton5ActionPerformed

